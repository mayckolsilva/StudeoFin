package uniftec.fabio.com.studeofin.vo;

public class UsuariosVO {

    private Integer idUsuario;
    private String desNome;
    private String desSobreNome;
    private String desEmail;
    private String desSenha;
    private String imgFoto;

    public Integer getIdUsuario() {
        return idUsuario;
    }

    public void setIdUsuario(Integer idUsuario) {
        this.idUsuario = idUsuario;
    }

    public String getDesNome() {
        return desNome;
    }

    public void setDesNome(String desNome) {
        this.desNome = desNome;
    }

    public String getDesSobreNome() {
        return desSobreNome;
    }

    public void setDesSobreNome(String desSobreNome) {
        this.desSobreNome = desSobreNome;
    }

    public String getDesEmail() {
        return desEmail;
    }

    public void setDesEmail(String desEmail) {
        this.desEmail = desEmail;
    }

    public String getDesSenha() {
        return desSenha;
    }

    public void setDesSenha(String desSenha) {
        this.desSenha = desSenha;
    }

    public String getImgFoto() { return imgFoto; }

    public void setImgFoto(String imgFoto) { this.imgFoto = imgFoto; }


}
